#include <stdio.h>

int main() {
    int num1, num2, num3, sum;

    // Assign values to the numbers
    num1 = 5;
    num2 = 10;
    num3 = 15;

    // Calculate the sum
    sum = num1 + num2 + num3;

    // Print the sum
    printf("The sum of %d, %d, and %d is %d\n", num1, num2, num3, sum);

    return 0;
}
